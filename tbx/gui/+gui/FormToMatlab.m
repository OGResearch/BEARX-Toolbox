
classdef FormToMatlab

    methods (Static)

        function out = separate(in, opt)
            arguments
                in (1, 1) string
                opt.RemoveEmpty (1, 1) logical = true
            end
            out = reshape(split(in, "+"), 1, []);
            if opt.RemoveEmpty
                out(out=="") = [];
            end
        end%

        function out = resolveSpecialCharacters(in)
            arguments
                in (1, :) string
            end
            table = gui.FormToMatlab.RESOLUTION_TABLE;
            out = regexprep(in, "%(..)", "${table.(""x"" + $1)}");
        end%

        function out = receive(in, options)
            arguments
                in (1, 1) string
                options.separate (1, 1) logical = true
                options.strip (1, 1) logical = true
            end
            out = in;
            if options.separate
                out = gui.FormToMatlab.separate(out);
            end
            out = gui.FormToMatlab.resolveSpecialCharacters(out);
            if options.strip
                out = strip(out);
            end
        end%

        function matlab = string(form)
            form = gui.FormToMatlab.receive(form, separate=false);
            matlab = form;
        end%

        function matlab = name(form)
            form = gui.FormToMatlab.receive(form);
            if isempty(form)
                form = "";
            end
            if numel(form) ~= 1
                error("Invalid name: %s", join(form, " "));
            end
            matlab = form;
            gui.FormToMatlab.validateNames(matlab, form);
        end%

        function matlab = names(form)
            form = gui.FormToMatlab.receive(form);
            if isempty(form)
                matlab = string.empty(1, 0);
                return
            end
            matlab = regexp(form, "[a-zA-Z_]\w*", "match", "once");
            matlab = reshape(matlab, 1, []);
            gui.FormToMatlab.validateNames(matlab, form);
        end%

        function validateNames(names, form)
            validIndex = arrayfun(@isvarname, names);
            if ~all(validIndex)
                error("Invalid name(s): %s", join(form, " "));
            end
        end%

        function matlab = resolveNumber(form)
            if isempty(form)
                form = "";
            end
            if numel(form) ~= 1
                error("Invalid number: %s", join(form, " "));
            end
            if form == ""
                matlab = NaN;
                return
            end
            if strip(lower(form)) == lower("NaN")
                matlab = NaN;
                return
            end
            matlab = double(form);
            if isnan(matlab)
                error("Invalid number: %s", join(form, " "));
            end
        end%

        function matlab = number(form)
            % "" --> NaN
            form = gui.FormToMatlab.receive(form);
            matlab = gui.FormToMatlab.resolveNumber(form);
        end%

        function matlab = numbers(form)
            % "" --> []
            form = gui.FormToMatlab.receive(form);
            if isempty(form) || isequal(form, "")
                matlab = double.empty(1, 0);
                return
            end
            matlab = nan(1, numel(form));
            for i = 1 : numel(form)
                matlab(i) = gui.FormToMatlab.resolveNumber(form(i));
            end
        end%

        function matlab = logical(form)
            form = gui.FormToMatlab.receive(form);
            if numel(form) ~= 1
                error("Invalid value; expected true or false");
            end
            matlab = gui.isTrue(form);
        end%

        function matlab = logicals(form)
            form = gui.FormToMatlab.receive(form);
            matlab = true(1, numel(form));
            for i = 1 : numel(form)
                matlab(i) = gui.isTrue(form(i));
            end
        end%

        function matlab = date(form)
            form = gui.FormToMatlab.receive(form);
            if numel(form) ~= 1
                error("Invalid date: %s", join(form, " "));
            end
            try
                datex(form);
            catch
                error("Invalid date: %s", join(form, " "));
            end
            matlab = form;
        end%

        function matlab = dates(form)
            form = gui.FormToMatlab.receive(form);
            try
                datex(form);
            catch
                error("Invalid dates: %s", join(form, " "));
            end
            matlab = form;
        end%

        function matlab = span(form)
            form = gui.FormToMatlab.receive(form);
            if numel(form) ~= 2
                error("Invalid date span: %s", join(form, " "));
            end
            try
                d = datex(form);
            catch
                error("Invalid date span: %s", join(form, " "));
            end
            if datex.frequency(d(1)) ~= datex.frequency(d(2))
                error("Invalid date span: %s", join(form, " "));
            end
            matlab = form;
        end%

        function matlab = filename(form)
            form = gui.FormToMatlab.receive(form);
            if numel(form) ~= 1
                error("Invalid filename: %s", join(form, " "));
            end
            matlab = form;
        end%

    end


